      $(function() {
          var state = false;
          var scrollpos;
          $('.toggle-btn').on('click', function(){
            // $('#common-header').toggleClass('open');
            if(state == false) {
              scrollpos = $(window).scrollTop();
              var navHeight = $('.global-nav').outerHeight();
              $('.wrapper').css({'overflow': 'hidden', 'height': navHeight + 'px'});
              $(window).scrollTop(0);
              $('#common-header').addClass('open');
              state = true;
            } else {
              $('.wrapper').css({'overflow': 'auto', 'height': 100 + '%'});
              $('#common-header').removeClass('open');
              window.scrollTo( 0 , scrollpos );
              state = false;
            }
          });
      });
    </script>

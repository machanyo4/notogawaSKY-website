    <script type="text/javascript">
      $(function() {
          var state = false;
          var scrollpos;
          $('.toggle-btn').on('click', function(){
            // $('#common-header').toggleClass('open');
            if(state == false) {
              scrollpos = $(window).scrollTop();
              var navHeight = $('.global-nav').outerHeight();
              $('.wrapper').css({'overflow': 'hidden', 'height': navHeight + 'px'});
              $(window).scrollTop(0);
              $('#common-header').addClass('open');
              state = true;
            } else {
              $('.wrapper').css({'overflow': 'auto', 'height': 100 + '%'});
              $('#common-header').removeClass('open');
              window.scrollTo( 0 , scrollpos );
              state = false;
            }
          });
      });
    </script>
    <script type="text/javascript">
      $(function(){
        var imgHeight = $('.page-title').outerHeight(); //画像の高さを取得。これがイベント発火位置になる。
        var header = $('.scroll-header'); //ヘッダーコンテンツ
        $(window).on('load scroll', function(){
           if ($(window).scrollTop() < imgHeight) {
             header.removeClass('show');
           }else {
             header.addClass('show');
           }
        });
      });
